"""
机器人搬砖,一共有N堆砖存放在N个不同的仓库中,第1堆砖中有bricks[i]块砖头,要求在8小时内搬完。机器人每
小时能搬砖的数量取决于有多少能量格,机器人一个小时中只能老在一个仓库中搬砖,机器人的能量格每小时补充一
次且能量格只在这一个小时有效,为使得机器人损耗最小化尽量减小每次补充的能量格数为了保障在8小时内能完成
搬砖任务,请计算每小时给机器人充能的最小能量格数
说明:
1、无需考虑机器人补充能量格的耗时
2、无需考虑机器人搬砖的耗时
3、机器人每小时补充能量格只在这一个小时中有效
输入输出Q
输入
第一行为一行数字,空格分隔
输出
机器人每小时最少需要充的能量格,若无法完成任务,车输出-1

1.我们需要找到最小的每小时能量(能量格数),使得机器人可以在8小时内搬运所有砖块。
2.由于机器人每小时的能量是固定的,我们可以从1开始逐渐增加每每小时的能量值,直到找到能够在8小时内完成搬砖任
务的最小能量值。
3.对于每一个尝试的能量值,我们计算机器人使用该能量值工作时每堆砖块所需的小时数,并将它们相加,得到总小
时数。
4.如果总小时数小于或等于8小时,则当前的能量值是有效的。我们继续尝试更小的能量值,直到找到最小的有效能量
值。
5.如果总小时数大于8小时,则当前的能量值太小,无法在8小时内完成任务。我们需要尝试更大的能量值。
"""


def can_complete(bricks, energy, hours):
    """
    检查给定的能量是否足以让机器人在规定时间内搬运所有砖块。
    :param bricks: 砖块堆的列表，每个数字代表一个砖块堆的砖块数量
    :param energy: 机器人每小时的能量值（每小时可以搬运的砖块数量）
    :param hours: 规定的时间（小时）
    :return: 布尔值，能否在规定时间内搬运所有砖块
    """
    total_hours = 0  # 总共需要的小时数
    for brick in bricks:
        total_hours += -(-brick // energy)  # 使用天花板除法计算每堆砖块需要的小时数
    return total_hours <= hours  # 如果总小时数小于等于规定时间，则返回True

def find_min_energy(bricks, hours):
    """
    找到机器人搬运所有砖块所需的最小能量值。
    :param bricks: 砖块堆的列表
    :param hours: 规定的时间（小时）
    :return: 最小的有效能量值
    """
    total_bricks = sum(bricks)  # 所有砖块的总和
    for energy in range(1, total_bricks + 1):  # 从1开始逐一尝试每个能量值
        if can_complete(bricks, energy, hours):  # 如果当前能量值有效
            return energy  # 返回当前的能量值作为最小有效能量值
    return -1  # 如果找不到有效的能量值，返回-1


bricks_input = input("请输入砖块堆的数量，使用空格分隔：")
hours_input = 8

# 将输入的砖块堆数量字符串转换为整数列表
bricks = [int(x) for x in bricks_input.split()]

# 调用函数并打印结果
min_energy = find_min_energy(bricks, hours_input)
print(f"{min_energy}")
