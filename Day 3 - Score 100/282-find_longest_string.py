"""
题目描述
给定一个字符串,只包含字母和数字,按要求找出字符串中的最长(连续)子串的长度,字符
身是其最长的子串,子串要求:
1、只包含1个字母(a~z,A~Z),其余必须是数字;
2、字母可以在子串中的任意位置;
如果找不到满足要求的子串,如全是字母或全是数字,则返I回-1
输入输出
输入
字符串(只包含字母和数字)
输出
子串的长度

1、初始化最长子串的长度为0
2、遍历字符串 s中的每个字符
3、如果当前字符是字母,我们将其作为子串的中心字符,并向前前后两个方向扩展,直到遇到
字字符或到达字符串的边界
4、在扩展过程中,我们分别用两个指针指示当前位置,通过判断新当前字符是否是数字字符来
否满足条件
5、我们不断更新满足条件的子串的长度,并将其与之前的最长子串长度进行比较,保留较大
最长子串的长度
6、如果最长子串的长度大于1,则返回该长度;否则,表示找不到满足条件的子串,返回-1

"""

def find_longest_substring(s):
    # 初始化最长子串的长度为0
    max_length = 0

    # 遍历字符串s
    for i in range(len(s)):
        # 如果当前字符是字母
        if s[i].isalpha():
            # 初始化子串长度为1，因为当前字符满足条件
            length = 1
            # 向前遍历，直到找到一个数字字符或到达字符串开头
            j = i - 1
            while j >= 0 and s[j].isdigit():
                length += 1
                j -= 1
            # 向后遍历，直到找到一个数字字符或到达字符串结尾
            j = i + 1
            while j < len(s) and s[j].isdigit():
                length += 1
                j += 1
            # 更新最长子串的长度
            max_length = max(max_length, length)

    # 如果最长子串的长度大于1，则返回最长子串的长度；否则，返回-1
    if max_length > 1:
        return max_length
    else:
        return -1



s = input("请输入一个字符串：")
result = find_longest_substring(s)
print("最长字母子串的长度为：", result)
