"""
题目描述
小扇和小船今天又玩起来了数字游戏，小船给小扇一个正整数n(1<=n<=e9)，小扇需要找到个比n大的数字m，使得m和n对应的二进制中1的个数要相同，如:
1、4对应二进制100
2、8对应二进制1000
3、其中1的个数都为1个
现在求m的最小值
输入输出
输入
一个正整数n(1<=n<=e9)
输出
一个正整数m

输入
2


输出
4

说明:
2的二进制10,4的二进制100,1的个数相同，且4是满足条件的最小数

输入
7


输出
11

说明:
7的二进制111,11的二进制1011,1的个数相同，且11是满足条件的最小数

Python国语言思路
使用位操作来解决这个问题可以大大提高效率。这个问题通常被称为"下一个更大数(NextGreaterNumber)“问题。关键在于找到最右边的非尾部0(即一个0，其右边至少有一个1)，然后将其翻转为1，接着将这个位右边的所有位重排，使得其1尽可能靠右(因为我们需要最小的更大数)。
位操作的步骤:
1.找到最右边的非尾部的位置(c0)和它右边的1的个数(c1)。
2.将该0翻转为1，即n|=(1<《<(o))。
3.将该位右边的所有位清零，即n&=~((1《<(ca))-1)。
4.将(c1-1)个1添加到该位的右边，即n1=(1《<(c1-1))-1。
"""

def next_number_with_same_bits_bitwise(n):
    # c0 和 c1 分别代表最右边的非尾部0的个数和它右边的1的个数
    c = n
    c0 = 0
    c1 = 0

    # 计算c0和c1
    while ((c & 1) == 0) and (c != 0):
        c0 += 1
        c >>= 1

    while (c & 1) == 1:
        c1 += 1
        c >>= 1

    # 错误情况，如果 n 是0或者形如11...1100...00，那么没有更大的数字与之有相同数目的1
    if c0 + c1 == 31 or c0 + c1 == 0:
        return -1

    # p是最右边的非尾部0的位置
    p = c0 + c1

    # 翻转最右边的非尾部0
    n |= (1 << p)
    # 将p右边的所有位清零
    n &= ~((1 << p) - 1)
    # 在右边插入(c1-1)个1
    n |= (1 << (c1 - 1)) - 1

    return n


# 读取输入
n = int(input())

# 计算结果
m = next_number_with_same_bits_bitwise(n)

# 输出结果
print(m)

