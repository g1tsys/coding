from itertools import combinations

def find_steps_combination(count, steps):
    # 生成所有可能的三元素组合的索引
    comb_indices = combinations(range(len(steps)), 3)
    min_index_sum = float('inf')
    result = []
    
    # 遍历所有组合
    for i, j, k in comb_indices:
        # 计算当前组合对应的步数总和
        if steps[i] + steps[j] + steps[k] == count:
            current_sum = i + j + k
            # 检查是否为最小索引和
            if current_sum < min_index_sum:
                min_index_sum = current_sum
                result = [steps[i], steps[j], steps[k]]
    
    return result