"""
如此，图像软件在打开此格式灰度图的时候，就可以根据此算法从压缩能数据恢复出原始灰度图矩阵
请从输入的压缩数恢复灰度图原始知阵，并返回指定像素的灰阶值

2,Python四语言思路
1.读取输入的压缩数据和像素位置数据。
2.提取压缩議据中的行数和列数，以及像素位置的行号和列号。
3.初始化一个空矩阵matrix，用于存放灰阶值。
4.从第三个数开始遍历压数据，每两个数一组，分别表示灰阶值和连续像素个数。
5.将连续的灰阶值按照连续像素个数添加到矩阵matrix中。
6.根据给定的像素位置，通过计算得到该像素在一维数组中的位置。
7.获取该位置对应的灰阶值。
8.输出灰阶值。
"""

# 输入处理
compressed_data = input().strip().split()  # 读取压缩数据并以空格分隔
pixel_position = input().strip().split()  # 读取像素位置数据并以空格分隔
rows, cols = int(compressed_data[0]), int(compressed_data[1])  # 获取矩阵的行和列数
pixel_row, pixel_col = int(pixel_position[0]), int(pixel_position[1])  # 获取指定像素的行号和列号

# 根据压缩数据恢复灰阶矩阵
matrix = []  # 初始化矩阵，用于存放灰阶值
i = 2  # 从第三个数开始，因为前两个数是行和列
while i < len(compressed_data):
    grayscale = int(compressed_data[i])  # 获取当前的灰阶值
    count = int(compressed_data[i+1])  # 获取连续像素个数
    matrix.extend([grayscale] * count)  # 将连续的灰阶值添加到矩阵中
    i += 2  # 移动到下一组数据

# 由于我们按照一维数组的方式存储了二维矩阵的数据，
# 我们需要根据行号和列号来计算所需像素在一维数组中的位置。
# 行号乘以每行的列数加上列号即得到一维数组中的位置。
# 注意，由于矩阵是从0开始索引的，所以我们用 pixel_row * cols + pixel_col
pixel_value = matrix[pixel_row * cols + pixel_col]  # 计算并获取指定像素的灰阶值

# 输出
print(pixel_value)

