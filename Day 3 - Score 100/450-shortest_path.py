"""
●题目描述
给定两个字符串，分别为字符串A与字符串B;例如A字符串为ABCABBA，B字符串为CBABAC可以得到下图m*n的二维数组，定义原点为(0，0)，终点为(m，n)，水平与垂直的每一条边距离为1，映射成坐标系如下图
从原点(0，0)到(0，A)为水平边，距离为1，从(0，A)到(A，C)为垂直边，距离为1;假设两人字符串同一位置的两个字符相同则可以作一个斜边，如(A，C)到(B，B)最短距离为斜边，距离同样为1;
作出所有的斜边如下图，(0，0)到(B，B)的距离为 1个水平边 +1个垂直边+1个斜边 =3
根据定义可知，原点到终点的最短距离路径如下图红线标记，最短距离为9

输入
ABC ABC


输出
3

输入
ABCAABA  CBABAC


输出
9

1.初始化边界条件:首先需要初始化二维数组 dp 的边界值。对于所有的 i，dp[i][@] 表示只沿着水平方向移动到(i，0)的距离，即i。同理，对于所有的 j，dp[0][j]表示只沿着垂直方向移动到(0，j)的距离，即j。
2.填充动态规划表:接下来，需要计算从(日，0)到(i，j)的最短距离。这里有两种情况:
如果 A[i-11 == B[j-11，即字符串 A的第i个字符和字符串 B的第 j个字符相同，那么可以从(i-1，j-1)通过一条斜线直接到达(i，j)，此时 dp[i][j] = dp[i-1][j-1]+1o
由
如果 A[i-11!= B[j-11，那么不能直接通过斜线到达(i，j)，需要选择水平移动(从(i-1，j)到(i，j))或者垂直移动(从(i，j-1)到(i，j))，选择这两种移动方式中距离更短的一种，即 dp[il[j]= min(dp[i-1][j]，dp[i][j-1])+ 1。
3.返回结果:经过以上步骤填充完整个动态规划表后，dp[m][n]就是从原点(0，0)到终点(m，n)的最短距离。

"""


def shortest_distance(A, B):
    m = len(A)
    n = len(B)

    # 创建一个 (m+1) x (n+1) 的二维数组 dp，所有元素初始化为0
    dp = [[0] * (n+1) for _ in range(m+1)]

    # 初始化边界条件，即只有水平或垂直移动的情况
    for i in range(1, m+1):
        dp[i][0] = i
    for j in range(1, n+1):
        dp[0][j] = j

    # 动态规划填表
    for i in range(1, m+1):
        for j in range(1, n+1):
            # 如果当前字符匹配，则可以斜线移动
            if A[i-1] == B[j-1]:
                dp[i][j] = dp[i-1][j-1] + 1
            else:
                # 否则，取水平移动和垂直移动中的最短路径
                dp[i][j] = min(dp[i-1][j], dp[i][j-1]) + 1

    # 返回从原点到终点的最短距离
    return dp[m][n]

# 输入样例
A,B=input().split()
m,n=len(A),len(B)

# 调用函数，并打印结果
print(shortest_distance(A, B))  
