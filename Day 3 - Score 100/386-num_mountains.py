def countPeaks(arr):
    n = len(arr)
    peaks = 0
    for i in range(n):
        # 判断是否为山峰
        if (i == 0 or arr[i] > arr[i-1]) and (i == n-1 or arr[i] > arr[i+1]):
            peaks += 1
    return peaks

# 示例输入
arr = list(map(int, input().strip().split(',')))
# 调用函数计算山峰个数
result = countPeaks(arr)
# 输出结果
print(result)
