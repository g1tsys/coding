from math import ceil

def count_min_kids(feedbacks):
    # 记录每个小区人数反馈的次数
    count_dict = {}
    for feedback in feedbacks:
        if feedback in count_dict:
            count_dict[feedback] += 1
        else:
            count_dict[feedback] = 1

    # 计算至少的小朋友数
    min_kids = 0
    for people, count in count_dict.items():
        # 向上取整计算小区数
        min_kids += ceil(count / (people + 1)) * (people + 1)

    return min_kids


