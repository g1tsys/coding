from collections import deque

# 动态规划解决问题
def maxScore(n, score, k):
    # 初始化dp数组
    dp = [0] * n
    dp[0] = score[0]

    # 初始化双端队列，并放入第一个元素的索引
    dq = deque([0])

    # 从第二个位置开始遍历
    for i in range(1, n):
        # 移除窗口之外的元素
        if dq[0] < i - k:
            dq.popleft()

        # 计算到达当前位置的最大得分
        dp[i] = score[i] + dp[dq[0]]

        # 维护双端队列，保证队列首元素是最大值的索引
        while dq and dp[i] >= dp[dq[-1]]:
            dq.pop()
        dq.append(i)

    # 返回到达终点的最大得分
    return dp[n-1]

