"""
给定一:一个正整数n，如果能够分解为m(m>1)个连续正整数之和，请输出所有分解中，m最小的分解
如果给定整数无法分解为连续正整数，则输出字符串"N"。
咖输入输出Q
输入输入数据为一整数，范围为(1，2^30]
输出最小的分解
输入
21


输出
21=10+11

输入
9


输出
9=4+5


说明:
9=2+3+4
9=4+5

输入
15


输出
15=7+8

说明:
15=1+2+3+4+5
15=4+5+6
15=7+8

说明:
21可以分解的连续正整数组合的形式有多种
21=1+2+3+4+5+6
21=6+7+8
21=10+11
因21=10+11,是最短的分解序列。所以答案是21=10+11


我们可以使用数学公式来帮助我们找到连续正整数之和的分解。给定正整数n，如果它可以被分解成m个连续正整数之和，那么这些连续整数可以表示为x,x+1,x+2,…, x+m-1，其中x是序列的第一个数。根据等差数列求和的公式，我们可以得到:
n=x+(x+1)+(x+2)+...+(x+m-1)n=mx+m(m-1)2
对于一个确定的m值，我们可以解出x的值:
x=(n-m(m-1)2)/m
如果x是正整数，那么我们就找到了一个有效的 回分解。
我们的目标是找到最小的m值，所以我们可以从m=2开始递增m的值，直到找到一个有效的Qx值。为了保证x是正整数，我们还需要确保n-m(m-1)/2能够被m整除
"""

def find_min_sequence(n):
    """
    寻找最小的连续整数分解

    :param n: 要分解的正整数
    :return: 最小分解的字符串表示，如果不存在则返回"N"
    """
    for m in range(2, n):
        # 通过等差数列求和公式来解出x
        x = (n - m * (m - 1) // 2) / m
        # 如果x是正整数，并且m使得x为正，那么我们找到了分解
        if x.is_integer() and x > 0:
            # 构造分解序列的字符串表示
            sequence = [str(int(x + i)) for i in range(m)]
            return f"{n}=" + "+".join(sequence)
    
    # 如果所有m都无法使得x为正整数，则返回"N"
    return "N"

# 样例输入
n = 21

# 样例输出
print(find_min_sequence(n))
