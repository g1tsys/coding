'''
在学校中,N个小朋友站成一队,第1个小朋友的身高为height[i1
第1个小朋友可以看到的第一个比自己身高更高的小朋友j,那么是i的好朋友
请重新生成一个列表,对应位置的输出是每个小朋友的好朋友位立置,如果没有看到好朋友,
该位置用0代替
输入输出
输入
第一行输入N,N表示有N个小朋友
第二行输入N个小朋友的身高height[i],都是整数
输出
输出N个小朋友的好朋友的位置

1、遍历小朋友的身高列表,对于每一个小朋友,我们要向认首的方向查找第一个比他身高更
朋友
2、我们使用一个找来辅助查找。找中保存的是小朋友的索引
3、从队首开始遍历小朋友的身高列表,对于当前小朋友,我们比较他的身高与找顶小朋友的!
4、如果当前小朋友的身高比栈顶小朋友的身高高,那么找顶小朋友就是当前小朋友的好朋友。
将栈顶元素出栈,并将栈顶小朋友的索引保存在结果列表中,表示示该小朋友的好朋友位置
5、如果当前小朋友的身高比栈顶小朋友的身高矮或者相等,我们继续向队首的方向查找,直至
比当前小朋友身高更高的小朋友或者栈为空
6、将当前小朋友的索引入栈,继续下一个小朋友的遍历
7、最后,遍历结束后,找中剩下的小朋友表示没有看到好朋友,将其对应的位置在结果列表
8、返回结果列表作为输出
'''

def find_good_friends(N, height):
    stack = []  # 用于保存小朋友的索引的栈
    result = [0] * N  # 初始化结果列表，初始值为0

    for i in range(N):
        # 当栈不为空且当前小朋友的身高大于栈顶小朋友的身高时，栈顶小朋友是当前小朋友的好朋友
        while stack and height[i] > height[stack[-1]]:
            j = stack.pop()  # 弹出栈顶元素
            result[j] = i  # 将栈顶小朋友的好朋友位置设为当前小朋友的索引

        stack.append(i)  # 将当前小朋友的索引入栈

    return result


# 输入
N = int(input())
height = list(map(int, input().split()))

# 调用函数并输出结果
result = find_good_friends(N, height)
print(*result)

