def simulate_motion(matrix, w, h, x, y, sx, sy, t):
    # 计数物体经过的1的个数
    count = 0

    # 检查初始位置是否为1
    if matrix[y][x] == '1':
        count += 1

    # 开始模拟t时间单位
    for _ in range(t):
        # 更新位置
        x += sx
        y += sy

        # 检查是否经过1
        if matrix[y][x] == '1':
            count += 1

        # 遇到矩阵边缘时反射
        if x == w - 1 or x == 0:
            sx *= -1
        if y == h - 1 or y == 0:
            sy *= -1

    return count


# 读取输入
first_line = input().split()
w, h, x, y, sx, sy, t = map(int, first_line)
matrix = [input() for _ in range(h)]

# 运行模拟并输出结果
result = simulate_motion(matrix, w, h, x, y, sx, sy, t)
print(result)

