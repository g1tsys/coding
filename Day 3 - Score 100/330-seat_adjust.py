'''
疫情期间课堂的座位进行了特殊的调整,不能出现两个同学紧挨着,必须隔至少一个空位
一次
给你一个整数数组desk表示当前座位的占座情况,由若干0和1组/成,其中0表示没有占位,
示占位。在不改变原有座位秩序情况下,还能安排坐几个人?2
输入输出区
输入
第一行是个子数组表示作为占座情况,由若干0和1组成,其中中0表示没有占,1表示占位
输出
输出数值表示还能坐几个人

1.将输入的字符串转换为整数数组。
2.初始化新增座位数为0。
3.获取座位数组的长度。
4.遍历座位数组:
。检查当前位置是否为空,并且检查左右两个位置是否为空。
。如果满足条件,则在该位置安排一个人,将该位置的值修改为1,并将新增座位数加1。
5.返回新增座位数。
'''

def max_new_students(desk):
    # 将字符串输入转换为整数数组
    desk = list(map(int, desk.split(',')))

    # 初始化新增座位数为0
    new_students = 0

    # 获取数组长度
    n = len(desk)

    # 遍历座位数组
    for i in range(n):
        # 检查当前位置是否为空，并且检查周围的座位
        if desk[i] == 0:
            # 检查左边的座位，如果是数组的开头或者左边的座位是空的
            if i == 0 or desk[i - 1] == 0:
                # 检查右边的座位，如果是数组的结尾或者右边的座位是空的
                if i == n - 1 or desk[i + 1] == 0:
                    # 如果左右都是空的，那么可以在此位置安排一个人
                    desk[i] = 1
                    new_students += 1

    # 返回可以新增的座位数
    return new_students


# 测试样例
print(max_new_students("1,1,1,1,1"))  

