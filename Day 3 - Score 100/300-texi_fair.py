'''
程序员小明打了一辆出租车去上班,出于职业敏感,他注意到这辆两出租车的计费表有点问题,总
是偏大,出租车司机解释说他不喜欢数字4,所以改装了计费表,任何数字位置遇到数字4就直
接跳过,其余功能都正常
比如:
1、23再多一块钱就变为25;
2、39再多一块钱变为50;
3、399再多一块钱变为500;
小明识破了司机的伎俩,准备利用自己的学识打败司机的阴谋,给合出计费表的表面读数,返回实
际产生的费用
输入输出区
输入
只有一行,数字N,表示里程表的读数(1<=N<=88888888888)
输出
一个数字,表示实际产生的费用,以回车结束

1.将输入的数字N转换为字符串以便逐位处理。
2.初始化变量real_cost为0,用于记录实际产生的费用。
3.遍历字符串中的每一位数字。
4.将当前位的数字转换为整数类型,并赋值给变量digit。
5.如果digit大于4,说明计费表跳过了4,所以将digit减去1。
6.将当前位的数字转换为十进制,并累加到real_cost中。这里的累加操作可以通过将real_cost乘以
9再加上digit来实现。
7.遍历完成后,real_cost即为实际产生的费用。
8.返回real_cost作为函数的输出。
'''

def skipped_number_to_real_cost(N):
    # 将N转为字符串以便逐位处理
    str_N = str(N)
    real_cost = 0
    # 遍历每一位数字
    for digit in str_N:
        digit = int(digit)
        # 如果数字大于4，那么在实际数中要减去1（因为跳过了4）
        if digit > 4:
            digit -= 1
        # 将当前位的数字转换为十进制，并累加到real_cost中
        real_cost = real_cost * 9 + digit
    return real_cost

# 测试输入
N = int(input())
print(skipped_number_to_real_cost(N))
