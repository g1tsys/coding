"""
咖题目描述
给定一段”密文"字符串s， 其中字符都是经过“密码本”映射的，现需要将密文解密并输出映射的规则(“a'~ ') 分别用(“1'~“9])表 示;(}~'z’)分别用("10*"~"26*")表示约束:映射始终唯一
咖输入输出Q
输入密文字符串
输出
明文字符串
备注
翻译后的文本长度在100以内

输入
20*19*20*


输出
代码1: tst
代码2: tst

输入
10*2*5*15


输出
代码1:jbe
代码2:jb*e*ae

输入
10*2*5*15*


输出
代码1:jbeo
代码2:jb*e*o

输入
10*2*5*15*26*


输出
代码1:jbeoz
代码2:jb*e*oz


1.定义一个空字符串plain text，用来存储解密后的明文。
2.初始化一个变量temp num，用来临时存储多位数的数字。
3.遍历密文字符串中的每一个字符。
4. 判断当前字符是否是数字。
。如果是数字，将其加到temp num中，以便后续拼接多位数的数字。
。如果不是数字，说明遇到了"*"符号，表示前面的数字表示一个字母。
5.将temp_num转换成整数，并清空temp num，以便存储下一个数字。
6.根据映射规则将数字转换为对应的字母。
。如果数字是1-9，直接将数字转换成"a'-T'
。如果数字是10-26，将数字转换成了-'Z。
7.将转换后的字母拼接到plain text字符串中。
8.重复步骤3-7，直到遍历完整个密文字符串。
9.返回plain text作为解密结果。
"""
def decrypt(cipher_text):
    # 定义一个空字符串，用来存储解密后的明文
    plain_text = ''
    # 初始化一个变量，用来临时存储多位数的数字
    temp_num = ''
    # 遍历密文中的每一个字符
    for char in cipher_text:
        # 判断当前字符是否是数字
        if char.isdigit():
            # 如果是数字，就将其加到temp_num中
            temp_num += char
        # 如果遇到"*"符号，说明前面的数字表示一个字母
        elif char == '*':
            # 将数字转换成整数
            num = int(temp_num)
            # 清空temp_num，以便存储下一个数字
            temp_num = ''
            # 根据映射规则将数字转换为对应的字母
            if num <= 9:  # 如果数字是1-9，直接将数字转换成'a'-'i'
                plain_text += chr(ord('a') + num - 1)
            else:  # 如果数字是10-26，将数字转换成'j'-'z'
                plain_text += chr(ord('j') + num - 10)
    return plain_text


# 从用户那里接收密文字符串作为输入
input_cipher = input("请输入密文字符串: ")

# 调用解密函数并输出结果
output_plain = decrypt(input_cipher)
print("明文字符串:", output_plain)

