'''
题目描述
公司分月饼,m个员工,买了n个月饼,m<=n,每个员工至少分一个月饼,但是也可以分到多
个,单人分到最多月饼的个数是Max1,单人分到第二多月饼个数是Max2
但需要满足Max1-Max2<=3,单人分到第n-1多月饼个数是Max(n-1),单人分到第n多月饼个数
是Max(n),想要满足Max(n-1)-Max(n)<=3,问有多少种分月饼的方法?
输入输出
输入
输入一行,为炽包含LRFBAC的字母序列,最大长度50,可重复
输出
输出最终状态

Python 闪语言思路
1.定义一个主函数 count_ways来计算分配月饼的方法数。
2.在主函数内部定义一个递归函数dfs,它有三个参数:
o employees:剩余需要分配月饼的员工数量。
remaining_cakes:剩余的月饼数量。
olast_cakes:上一个员工分到的月饼数量。
3.检查是否已经没有员工需要分配月饼。如果 employees为0:
。如果剩余月饼也为0,说明当前的分配方案是合法的,返回1。
。否则,返回0,表示当前分支没有合法的分配方案。
4.如果剩余的月饼数少于剩余的员工数,说明不可能每个员工至少分到一个月饼,返回0。
5.初始化一个计数器ways,用于统计当前层的所有分配方案数
6.计算当前员工可以分到的月饼数量的下界,至少为1或」上一个员工分到的月饼数。
7.计算当前员工可以分到的月饼数量的上界,最多为 lastcakes+3,并且保证剩余月饼足够分
配给其他员工。
8.遍历当前员工可以分到的所有月饼数量。
9.对于每个可能的分配数量1,递归调用dfs,计算剩余)月饼分配给剩余员工的方案数,并将结果
累加到ways中。
10.返回当前层的所有分配方案的总数。
11.在主函数中调用dfs,传入初始参数m、n和1,并返回结果。
12.读取输入,获取员工数量m和月饼数量n。
13.调用 count_ways函数计算分配方案数,并输出结果。
'''

def count_ways(m, n):
    # 定义一个递归函数，用于计算分配方案的数量
    def dfs(employees, remaining_cakes, last_cakes):
        # 如果已经没有员工需要分配月饼
        if employees == 0:
            # 如果月饼也刚好分完，那么这是一个合法的分配方案
            return 1 if remaining_cakes == 0 else 0
        # 如果剩余的月饼数不足以分配给每个员工至少一个月饼
        if remaining_cakes < employees:
            return 0  # 这种情况下不可能有合法的分配方案
        ways = 0  # 初始化分配方案的计数器
        # 计算当前员工可以分到的月饼数量的下界，至少分一个
        start = max(1, last_cakes)
        # 计算当前员工可以分到的月饼数量的上界
        # 最多分到 last_cakes + 3，同时保证剩下的月饼足够分配给其他员工
        end = min(remaining_cakes - employees + 1, last_cakes + 3)
        # 遍历当前员工可以分到的所有月饼数量
        for i in range(start, end + 1):
            # 递归调用，计算分配剩余月饼给剩余员工的方案数
            ways += dfs(employees - 1, remaining_cakes - i, i)
        return ways  # 返回当前层的所有分配方案的总数

    return dfs(m, n, 1)  # 调用递归函数，并返回结果

# 读取输入
m, n = map(int, input().split())

# 计算并输出结果
print(count_ways(m, n))

