'''
题目描述
给定一个非空数组(列表),起元素数据类型为整型,请按照数组元素十进制最低位从小到大进行
排序,十进制最低位相同的元素,相对位置保持不变当数组元素为负值时,十进制最低为等同于
去除符号位后对应十进制值最低位
输入输出
输入
给定一个非空数组(列表)其元素数据类型为32位有符号整数
排序后的数组

1.首先,读取输入的字符串。
2.将字符串按逗号分隔为整数列表,使用split方法将字符串按逗号分隔,然后使用map图数将每
个分隔的元素转换为整数。
3.使用sorted函数对整数列表进行排序,传入一个lambdai函数作为排序的关键字。在lambda函数
中,使用取模运算%10获取整数的最低位,并且当整数为负数时,取绝对值再取模。
4.将排序后的整数列表转换为字符串,使用map函数将每个整数转换为字符串,然后使用join函数
将字符串列表用逗号连接起来。
5.输出排序后的字符串。
'''

# 读取输入的字符串
input_str = input()

# 将字符串按逗号分隔为整数列表
nums = list(map(int, input_str.split(',')))

# 使用lambda函数对列表进行排序，按最低位从小到大排序
sorted_nums = sorted(nums, key=lambda x: abs(x) % 10)

# 将排序后的整数列表转换为字符串，并用逗号分隔元素
output_str = ','.join(map(str, sorted_nums))

# 输出排序后的字符串
print(output_str)

